public class Task {
    public static void main(String[] args) {
        String A = "a";
        char a = A.charAt(0);
        System.out.println(a);
        char charExpression = charExpression(a);
        System.out.println(charExpression);
    }
    public static char charExpression(int a) {
        String slash = "/";
        char Slash = slash.charAt(0);
        System.out.println(Slash);
        return (char) (a + Slash);
    }
}
